package in.vamsoft.training.general;

public class Math {
  /**
   * Simple method add two integer element
   * 
   * @author vamsoft.
   * @param x.
   * @param y.
   * @return.
   */
  public int sum(int x, int y) {

    return x + y;
  }

  /**
   * simple method add two double element
   * 
   * @param a.
   * @param b.
   * @return.
   */
  public double sum(double a, double b) {
    return a + b;
  }

}